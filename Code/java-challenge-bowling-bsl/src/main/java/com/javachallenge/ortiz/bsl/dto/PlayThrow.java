public class PlayThrow {

	int actualThrow;
	int firstNextThrow;
	int secondNextThrow;
	String actualThrowOrg;
	String firstNextThrowOrg;
	String secondNextThrowOrg;

	public int getActualThrow() {
		return actualThrow;
	}

	public void setActualThrow(int actualThrow) {
		this.actualThrow = actualThrow;
	}

	public int getFirstNextThrow() {
		return firstNextThrow;
	}

	public void setFirstNextThrow(int firstNextThrow) {
		this.firstNextThrow = firstNextThrow;
	}

	public int getSecondNextThrow() {
		return secondNextThrow;
	}

	public void setSecondNextThrow(int secondNextThrow) {
		this.secondNextThrow = secondNextThrow;
	}

	public String getActualThrowOrg() {
		return actualThrowOrg;
	}

	public void setActualThrowOrg(String actualThrowOrg) {
		this.actualThrowOrg = actualThrowOrg;
	}

	public String getFirstNextThrowOrg() {
		return firstNextThrowOrg;
	}

	public void setFirstNextThrowOrg(String firstNextThrowOrg) {
		this.firstNextThrowOrg = firstNextThrowOrg;
	}

	public String getSecondNextThrowOrg() {
		return secondNextThrowOrg;
	}

	public void setSecondNextThrowOrg(String secondNextThrowOrg) {
		this.secondNextThrowOrg = secondNextThrowOrg;
	}

}
